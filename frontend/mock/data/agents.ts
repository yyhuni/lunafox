export function getMockAgents(page = 1, pageSize = 10, status?: string): AgentsResponse {
  const filtered = status ? mockAgents.filter((agent) => agent.status === status) : mockAgents
  const total = filtered.length
  const totalPages = Math.ceil(total / pageSize) || 1
  const start = (page - 1) * pageSize
  const results = filtered.slice(start, start + pageSize)

  return {
    results,
    total,
    page,
    pageSize,
    totalPages,
  }
}

export function getMockAgentById(id: number): Agent | undefined {
  return mockAgents.find((agent) => agent.id === id)
}

